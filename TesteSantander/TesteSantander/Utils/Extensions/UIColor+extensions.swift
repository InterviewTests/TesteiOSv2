import Foundation
import UIKit

extension UIColor {
    
    @nonobjc class var warmBlue: UIColor {
        return UIColor(red: 59.0 / 255.0, green: 73.0 / 255.0, blue: 238.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var white: UIColor {
        return UIColor(white: 1.0, alpha: 1.0)
    }
    
    @nonobjc class var whiteTwo: UIColor {
        return UIColor(white: 254.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var lightGreyBlue: UIColor {
        return UIColor(red: 168.0 / 255.0, green: 180.0 / 255.0, blue: 196.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var slate: UIColor {
        return UIColor(red: 72.0 / 255.0, green: 84.0 / 255.0, blue: 101.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var lightPeriwinkle: UIColor {
        return UIColor(red: 220.0 / 255.0, green: 226.0 / 255.0, blue: 238.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var lightPeriwinkle30: UIColor {
        return UIColor(red: 219.0 / 255.0, green: 223.0 / 255.0, blue: 227.0 / 255.0, alpha: 0.3)
    }
    
    @nonobjc class var lightSkyBlue: UIColor {
        return UIColor(red: 217.0 / 255.0, green: 226.0 / 255.0, blue: 233.0 / 255.0, alpha: 1)
    }
    
}
